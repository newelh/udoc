/// Look up the width of a character code in a standard PDF font.
///
/// Returns `None` if the font name is not one of the 14 standard fonts
/// or if the character code has no defined width.
///
/// Font name should have subset prefix already stripped
/// (e.g. "Helvetica" not "ABCDEF+Helvetica").
pub fn standard_width(font_name: &str, char_code: u8) -> Option<u16> {
    let table = table_for_font(font_name)?;
    let w = table[char_code as usize];
    if w > 0 {
        Some(w)
    } else {
        None
    }
}

/// Returns true if the given font name is one of the 14 PDF standard fonts.
pub fn is_standard_font(font_name: &str) -> bool {
    table_for_font(font_name).is_some()
}

fn table_for_font(name: &str) -> Option<&'static [u16; 256]> {
    // All Courier variants are monospaced with identical widths.
    // Helvetica-Oblique has identical widths to Helvetica.
    // Helvetica-BoldOblique has identical widths to Helvetica-Bold.
    match name {
        "Courier" | "Courier-Bold" | "Courier-Oblique" | "Courier-BoldOblique" => Some(&COURIER),
        "Helvetica" | "Helvetica-Oblique" => Some(&HELVETICA),
        "Helvetica-Bold" | "Helvetica-BoldOblique" => Some(&HELVETICA_BOLD),
        "Times-Roman" => Some(&TIMES_ROMAN),
        "Times-Bold" => Some(&TIMES_BOLD),
        "Times-Italic" => Some(&TIMES_ITALIC),
        "Times-BoldItalic" => Some(&TIMES_BOLD_ITALIC),
        "Symbol" => Some(&SYMBOL),
        "ZapfDingbats" => Some(&ZAPF_DINGBATS),
        _ => None,
    }
}

