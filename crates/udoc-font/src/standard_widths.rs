#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_helvetica_key_widths() {
        // 'i' is narrow, 'm' is wide, space is 278
        assert_eq!(standard_width("Helvetica", b'i'), Some(222));
        assert_eq!(standard_width("Helvetica", b'm'), Some(833));
        assert_eq!(standard_width("Helvetica", b' '), Some(278));
        assert_eq!(standard_width("Helvetica", b'A'), Some(667));
    }

    #[test]
    fn test_courier_monospaced() {
        // All printable characters should be 600
        assert_eq!(standard_width("Courier", b'A'), Some(600));
        assert_eq!(standard_width("Courier", b'i'), Some(600));
        assert_eq!(standard_width("Courier", b' '), Some(600));
        assert_eq!(standard_width("Courier", b'W'), Some(600));
    }

    #[test]
    fn test_courier_variants_identical() {
        for code in 0u8..=255 {
            let base = standard_width("Courier", code);
            assert_eq!(base, standard_width("Courier-Bold", code));
            assert_eq!(base, standard_width("Courier-Oblique", code));
            assert_eq!(base, standard_width("Courier-BoldOblique", code));
        }
    }

    #[test]
    fn test_helvetica_oblique_same_as_upright() {
        for code in 0u8..=255 {
            assert_eq!(
                standard_width("Helvetica", code),
                standard_width("Helvetica-Oblique", code)
            );
            assert_eq!(
                standard_width("Helvetica-Bold", code),
                standard_width("Helvetica-BoldOblique", code)
            );
        }
    }

    #[test]
    fn test_times_roman_space() {
        assert_eq!(standard_width("Times-Roman", b' '), Some(250));
        assert_eq!(standard_width("Times-Bold", b' '), Some(250));
        assert_eq!(standard_width("Times-Italic", b' '), Some(250));
        assert_eq!(standard_width("Times-BoldItalic", b' '), Some(250));
    }

    #[test]
    fn test_unknown_font_returns_none() {
        assert_eq!(standard_width("Arial", b'A'), None);
        assert_eq!(standard_width("ComicSans", b' '), None);
    }

    #[test]
    fn test_control_chars_return_none() {
        // Control characters (0-31) should have no width
        for code in 0u8..32 {
            assert_eq!(standard_width("Helvetica", code), None);
        }
    }

    #[test]
    fn test_symbol_space() {
        assert_eq!(standard_width("Symbol", b' '), Some(250));
    }

    #[test]
    fn test_zapfdingbats_space() {
        assert_eq!(standard_width("ZapfDingbats", b' '), Some(278));
    }

    #[test]
    fn test_is_standard_font() {
        assert!(is_standard_font("Helvetica"));
        assert!(is_standard_font("Courier-Bold"));
        assert!(is_standard_font("Times-BoldItalic"));
        assert!(is_standard_font("Symbol"));
        assert!(is_standard_font("ZapfDingbats"));
        assert!(!is_standard_font("Arial"));
        assert!(!is_standard_font(""));
    }

    #[test]
    fn test_extended_range_populated() {
        // Accented chars should have widths (WinAnsi 0xC0+ range)
        // Agrave (0xC0) should match A width for Helvetica
        assert_eq!(standard_width("Helvetica", 0xC0), Some(667)); // Agrave = A
        assert_eq!(standard_width("Helvetica", 0xE0), Some(556)); // agrave = a
                                                                  // Euro sign at 0x80
        assert_eq!(standard_width("Helvetica", 0x80), Some(556));
    }
}
