#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_lookup_identity_h() {
        let cmap = lookup_predefined_cmap("Identity-H").unwrap();
        assert_eq!(cmap.name, "Identity-H");
        assert_eq!(cmap.code_length, 2);
        assert!(!cmap.is_vertical);
        assert!(cmap.is_identity);
    }

    #[test]
    fn test_lookup_identity_v() {
        let cmap = lookup_predefined_cmap("Identity-V").unwrap();
        assert!(cmap.is_vertical);
        assert!(cmap.is_identity);
    }

    #[test]
    fn test_lookup_unknown() {
        assert!(lookup_predefined_cmap("CustomEmbedded-H").is_none());
    }

    #[test]
    fn test_lookup_japanese() {
        let cmap = lookup_predefined_cmap("UniJIS-UTF16-H").unwrap();
        assert_eq!(cmap.code_length, 2);
        assert!(!cmap.is_identity);
    }

    #[test]
    fn test_is_vertical_cmap() {
        assert!(!is_vertical_cmap("Identity-H"));
        assert!(is_vertical_cmap("Identity-V"));
        assert!(is_vertical_cmap("UniJIS-UTF16-V"));
        assert!(!is_vertical_cmap("UniJIS-UTF16-H"));
    }

    #[test]
    fn test_cid_to_unicode_fallback_identity() {
        // Identity CMaps: CID often equals Unicode code point
        assert_eq!(cid_to_unicode_fallback(0x41, "Identity-H"), Some('A'));
        assert_eq!(
            cid_to_unicode_fallback(0x4E2D, "Identity-H"),
            Some('\u{4E2D}')
        ); // Chinese char
        assert_eq!(cid_to_unicode_fallback(0x20, "Identity-V"), Some(' '));
    }

    #[test]
    fn test_cid_to_unicode_fallback_non_identity() {
        // Non-identity CMaps: can't do direct mapping
        assert_eq!(cid_to_unicode_fallback(0x41, "90ms-RKSJ-H"), None);
        assert_eq!(cid_to_unicode_fallback(0x41, "UniJIS-UTF16-H"), None);
    }

    #[test]
    fn test_cid_to_unicode_fallback_invalid_scalar() {
        // Surrogate range is not a valid Unicode scalar
        assert_eq!(cid_to_unicode_fallback(0xD800, "Identity-H"), None);
        assert_eq!(cid_to_unicode_fallback(0xDFFF, "Identity-H"), None);
        // Beyond Unicode range
        assert_eq!(cid_to_unicode_fallback(0x110000, "Identity-H"), None);
    }

    #[test]
    fn test_cid_to_unicode_fallback_control_chars() {
        // Common whitespace passes through
        assert_eq!(cid_to_unicode_fallback(0x09, "Identity-H"), Some('\t'));
        assert_eq!(cid_to_unicode_fallback(0x0A, "Identity-H"), Some('\n'));
        assert_eq!(cid_to_unicode_fallback(0x0D, "Identity-H"), Some('\r'));
        // Other C0 control chars are rejected
        assert_eq!(cid_to_unicode_fallback(0x01, "Identity-H"), None);
        assert_eq!(cid_to_unicode_fallback(0x08, "Identity-H"), None); // backspace
        assert_eq!(cid_to_unicode_fallback(0x1F, "Identity-H"), None);
        // Null is rejected
        assert_eq!(cid_to_unicode_fallback(0x00, "Identity-H"), None);
        // C1 control chars are rejected
        assert_eq!(cid_to_unicode_fallback(0x80, "Identity-H"), None);
        assert_eq!(cid_to_unicode_fallback(0x9F, "Identity-H"), None);
        // Noncharacters are rejected
        assert_eq!(cid_to_unicode_fallback(0xFFFE, "Identity-H"), None);
        assert_eq!(cid_to_unicode_fallback(0xFFFF, "Identity-H"), None);
        assert_eq!(cid_to_unicode_fallback(0xFDD0, "Identity-H"), None);
        assert_eq!(cid_to_unicode_fallback(0xFDEF, "Identity-H"), None);
        assert_eq!(cid_to_unicode_fallback(0x1FFFE, "Identity-H"), None);
    }

    #[test]
    fn test_all_vertical_cmaps_detected() {
        for cmap in PREDEFINED_CMAPS {
            assert_eq!(
                cmap.is_vertical,
                is_vertical_cmap(cmap.name),
                "CMap {} vertical flag mismatch",
                cmap.name
            );
        }
    }
}
