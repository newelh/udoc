//! Predefined CMap registry for CID font character code decoding.
//!
//! PDF composite fonts use CMaps to map character codes (byte sequences
//! from content streams) to CIDs (character identifiers). This module
//! provides lookup tables for predefined CMaps that the PDF spec requires
//! viewers to support.
//!
//! For text extraction, we need two things from a CMap:
//! 1. **Codespace ranges**: how many bytes per character code
//! 2. **CID-to-Unicode fallback**: when ToUnicode is missing, map CID -> Unicode
//!
//! Identity-H/V are by far the most common in modern PDFs. They use a simple
//! 2-byte identity mapping (character code == CID). When the font also lacks
//! ToUnicode, we try interpreting the CID directly as a Unicode code point,
//! which works for most modern PDF generators.

/// Information about a predefined CMap encoding.
#[derive(Debug, Clone)]
pub struct PredefinedCMap {
    /// CMap name (e.g. "Identity-H").
    pub name: &'static str,
    /// Number of bytes per character code. For variable-length CMaps this
    /// is the maximum; callers must consult codespace ranges.
    pub code_length: u8,
    /// Whether this is a vertical writing CMap.
    pub is_vertical: bool,
    /// Whether character codes map directly to CIDs (identity mapping).
    /// True for Identity-H/V; false for CMaps with explicit code-to-CID tables.
    pub is_identity: bool,
}

/// Look up a predefined CMap by its encoding name.
///
/// Returns `None` for unknown or embedded CMaps (which must be parsed
/// from the PDF's CMap stream).
pub fn lookup_predefined_cmap(name: &str) -> Option<&'static PredefinedCMap> {
    PREDEFINED_CMAPS.iter().find(|cmap| cmap.name == name)
}

/// Whether the given CMap name is a vertical writing CMap.
///
/// Convention: CMap names ending in "-V" are vertical.
pub fn is_vertical_cmap(name: &str) -> bool {
    name.ends_with("-V")
}

/// Try to map a CID to a Unicode character without ToUnicode.
///
/// This is a best-effort heuristic for Identity-H/V encodings where
/// the CID often IS the Unicode code point (very common in modern PDFs
/// generated by Chrome, XeLaTeX, Word, etc.).
///
/// Returns None if the CID doesn't correspond to a valid Unicode scalar.
pub fn cid_to_unicode_fallback(cid: u32, cmap_name: &str) -> Option<char> {
    // For Identity encodings, try direct Unicode interpretation
    if cmap_name.starts_with("Identity") {
        let c = char::from_u32(cid)?;
        // Reject null, C0/C1 control characters (except common whitespace),
        // and Unicode noncharacters (U+FFFE, U+FFFF, etc.)
        if cid == 0 || is_noncharacter(cid) {
            return None;
        }
        if c.is_control() && !matches!(c, '\t' | '\n' | '\r') {
            return None;
        }
        return Some(c);
    }

    // For other known CMaps, direct interpretation is wrong (CIDs are
    // registry-specific). We'd need full CID-to-Unicode tables.
    // Return None to fall back to U+FFFD.
    None
}

/// Check if a code point is a Unicode noncharacter.
/// Noncharacters: U+FFFE, U+FFFF, and U+xFFFE/U+xFFFF for each plane,
/// plus U+FDD0..U+FDEF.
fn is_noncharacter(cp: u32) -> bool {
    (cp & 0xFFFE) == 0xFFFE || (0xFDD0..=0xFDEF).contains(&cp)
}

